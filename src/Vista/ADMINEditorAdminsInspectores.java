    private void ButtonModificarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarFuncionarioActionPerformed
        // TODO add your handling code here:
        // Verificar que se haya seleccionado una fila de la tabla
        int filaSeleccionada = TablaUsuarios.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje de advertencia
            javax.swing.JOptionPane.showMessageDialog(null, "Debe seleccionar un usuario para modificar.");
        } else {
            // Obtener los datos del empleado seleccionado en la fila
            String username = TablaUsuarios.getValueAt(filaSeleccionada, 0).toString();
            
            // Crear una instancia de la ventana de modificación y pasarle el empleado
            ADMINModificarFuncionario verModificarUsuario = new ADMINModificarFuncionario(obra, username);
            verModificarUsuario.setVisible(true);
        }
    }//GEN-LAST:event_ButtonModificarFuncionarioActionPerformed

    private void ButtonEliminarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarFuncionarioActionPerformed
        // TODO add your handling code here:
        // Verificar que se haya seleccionado una fila de la tabla
        int filaSeleccionada = TablaUsuarios.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje de advertencia
            javax.swing.JOptionPane.showMessageDialog(null, "Debe seleccionar un usuario para eliminar.");
        } else {
            // Obtener los datos del empleado seleccionado en la fila
            String identificacion = TablaUsuarios.getValueAt(filaSeleccionada, 0).toString();
            
            obra.eliminarUsuario(identificacion);
            javax.swing.JOptionPane.showMessageDialog(null, "Usuario eliminado exitosamente");
        }
        
    }//GEN-LAST:event_ButtonEliminarFuncionarioActionPerformed

