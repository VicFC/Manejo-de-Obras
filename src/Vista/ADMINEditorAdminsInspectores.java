    // Método para rellenar la tabla con los datos del ArrayList<Empleado>
private void cargarUsuariosEnTabla() {
    // Obtener el modelo de la tabla
    DefaultTableModel modelo = (DefaultTableModel) TablaUsuarios.getModel();

    // Limpiar cualquier dato previo en la tabla
    modelo.setRowCount(0);

    // Obtener la lista de empleados
    ArrayList<Usuario> usuarios = obra.getListaUsuarios();

    // Recorrer el ArrayList y agregar los datos a la tabla
    for (Usuario usuario : usuarios) {
        // Crear un arreglo con los datos del empleado
        Object[] fila = new Object[] {
            usuario.getUsername(),
            usuario.getPassword(),
            usuario.getNombre(),
            usuario.getRol(),
        };

        // Agregar la fila al modelo de la tabla
        modelo.addRow(fila);
    }
}

